ALTER PROC PRINT_ONE_TO_N
@N INT,@I INT
AS
BEGIN 
SET @I=0
WHILE @I<@N
	BEGIN
	PRINT @I+1;
	SET @I=@I+1;
	END
END
GO

EXEC PRINT_ONE_TO_N 10,0
GO


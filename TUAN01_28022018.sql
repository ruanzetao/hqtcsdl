CREATE PROC TIMMAX
	@S1 INT, @S2 INT,@MAX INT OUT
AS
BEGIN
	IF @S1>@S2 BEGIN
		SET @MAX=@S1
	END
	ELSE BEGIN
		SET @MAX=@S2
	END
	PRINT @MAX;
END
GO

